export const calculateTotalScore = (scores: SegakScore): number => {
  return scores.bmi + scores.naikTurunBangku + scores.tekanTubi + scores.ringkukTubi + scores.jangkauanMelunjur;
};

export const getFitnessLevel = (totalScore: number): { level: string; color: string } => {
  if (totalScore >= 21) return { level: 'Cemerlang', color: 'text-blue-500' };
  if (totalScore >= 16) return { level: 'Sangat Baik', color: 'text-green-500' };
  if (totalScore >= 11) return { level: 'Baik', color: 'text-yellow-500' };
  if (totalScore >= 6) return { level: 'Kurang Cergas', color: 'text-orange-500' };
  return { level: 'Tidak Cergas', color: 'text-red-500' };
};
